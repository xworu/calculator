def fn(fst_arg, opr, snd_arg):
        
    if opr == '+':
        return fst_arg + snd_arg
                 
    elif opr == '-':
        return fst_arg - snd_arg
                          
    elif opr == '*':
        return fst_arg * snd_arg
                                   
    if opr == '/':
        return fst_arg / snd_arg
